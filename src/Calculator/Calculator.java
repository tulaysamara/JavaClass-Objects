package Calculator;

public class Calculator {

	// add
	public int add(int num1, int num2) {
		return num1 + num2;
	}

	// sub
	public int subtract(int num1, int num2) {
		return num1 - num2;
	}
	
	//multiply
	public int multiply(int num1, int num2) {
		return num1 * num2;
	}
	
	//divide
	public int divide(int num1, int num2) {
		return num1 / num2;	
		}
	//square
	public int square(int num1) {
		return num1 * num1;
	}
	
	//squareroot
	
	}
	

